/**
 * Redis TTL (Time To Live) 常量定義
 * 
 * 所有時間單位為秒（seconds）
 * 
 * 使用方式：
 * ```typescript
 * import { TTL } from '@suggar-daddy/redis/constants';
 * 
 * await redis.set('user:profile:123', data, TTL.ONE_HOUR);
 * ```
 */

export const TTL = {
  // ══════════════════════════════════════════════════════════════
  // 超短期（Immediate）- 用於實時性要求高的數據
  // ══════════════════════════════════════════════════════════════
  
  /** 1 分鐘 (60 秒) - 輸入狀態、在線狀態 */
  ONE_MINUTE: 60,
  
  /** 5 分鐘 (300 秒) - 列表快取、搜尋結果 */
  FIVE_MINUTES: 5 * 60,
  
  /** 10 分鐘 (600 秒) - GEO 位置、附近用戶 */
  TEN_MINUTES: 10 * 60,
  
  /** 15 分鐘 (900 秒) - 驗證碼、配對推薦 */
  FIFTEEN_MINUTES: 15 * 60,
  
  // ══════════════════════════════════════════════════════════════
  // 短期（Short-term）- 用於頻繁更新的快取數據
  // ══════════════════════════════════════════════════════════════
  
  /** 30 分鐘 (1800 秒) - 熱點數據、支付訂單 */
  THIRTY_MINUTES: 30 * 60,
  
  /** 1 小時 (3600 秒) - 用戶資料快取、Access Token */
  ONE_HOUR: 60 * 60,
  
  /** 2 小時 (7200 秒) - 計數器、分數快取 */
  TWO_HOURS: 2 * 60 * 60,
  
  /** 6 小時 (21600 秒) - 靜態內容、配置快取 */
  SIX_HOURS: 6 * 60 * 60,
  
  // ══════════════════════════════════════════════════════════════
  // 中期（Medium-term）- 用於較穩定的數據
  // ══════════════════════════════════════════════════════════════
  
  /** 12 小時 (43200 秒) - 統計數據、報表快取 */
  TWELVE_HOURS: 12 * 60 * 60,
  
  /** 1 天 (86400 秒) - 用戶偏好、訂閱狀態 */
  ONE_DAY: 24 * 60 * 60,
  
  /** 3 天 (259200 秒) - Session、長期快取 */
  THREE_DAYS: 3 * 24 * 60 * 60,
  
  // ══════════════════════════════════════════════════════════════
  // 長期（Long-term）- 用於相對靜態的數據
  // ══════════════════════════════════════════════════════════════
  
  /** 1 週 (604800 秒) - Refresh Token、訂閱方案 */
  ONE_WEEK: 7 * 24 * 60 * 60,
  
  /** 2 週 (1209600 秒) - 試用期標記、長期會話 */
  TWO_WEEKS: 14 * 24 * 60 * 60,
  
  /** 1 個月 (2592000 秒) - 推送 Token、歷史記錄 */
  ONE_MONTH: 30 * 24 * 60 * 60,
} as const;

/**
 * TTL 類型定義
 */
export type TTLValue = typeof TTL[keyof typeof TTL];
